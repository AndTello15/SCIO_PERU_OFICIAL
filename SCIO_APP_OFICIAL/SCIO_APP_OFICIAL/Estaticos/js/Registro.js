function AdolesCursos() {
    var curAdolescencia = document.getElementById("AdolescenciaCursos");
    if (curAdolescencia.style.display == "none") {
        curAdolescencia.style.display = "block";
    }
    else {
        curAdolescencia.style.display = "none";
    }
}

